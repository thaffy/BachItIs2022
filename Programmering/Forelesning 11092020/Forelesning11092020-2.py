#Multiplikasjon, *
produkt=8*7
print('Produktet er',produkt)
print() #Dette er en tom linje

#Divisjon, /
resultat=76/4
print('Resultatet av 76:4 er',resultat)
print()

#Heltallsdivisjon, //
resultat=77/4
heltallsdivisjon=77//4
print('Resultatet av 77:4 er',resultat)
print('Heltallsdivisjonen av 77:4 er',heltallsdivisjon)
print()

#Restdivisjon, %
resultat=77/4
restdivisjon=77%4
print('Resultatet av 77:4 er',resultat)
print('Restdivisjonen av 77:4 er,',restdivisjon)

