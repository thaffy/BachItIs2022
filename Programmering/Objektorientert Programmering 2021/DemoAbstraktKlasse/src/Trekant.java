
public class Trekant extends Figur {
	private double høyde;
	private double lengde;
	

	public Trekant(double høyde, double lengde) {
		super();
		this.høyde = høyde;
		this.lengde = lengde;
	}

	// Implementerer beregnAreal():
	@Override
	public double beregnAreal() {
		return lengde*høyde/2;
	}

}
